# Задача 8. Декодирование битового поля
# Способ 1

def main(s):
    x = int(s, 16)

    v2 = (x >> 2) & 0x7F    # 111 1111
    v3 = (x >> 9) & 0x07    # 111
    v4 = (x >> 12) & 0xFF   # 1111 1111
    v5 = (x >> 20) & 0xFF   # 1111 1111
    v6 = (x >> 28) & 0x07   # 111

    return (v2, v3, v4, v5, v6)


print(
    main('0x563a931b'),
    main('0x42c9e949'),
    main('0x5ba87a22'),
    main('0x70070'),
    sep='\n'
)
